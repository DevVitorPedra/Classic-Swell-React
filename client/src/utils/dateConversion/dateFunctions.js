export const forecastDays = (date) =>{
    if(date===0){  
        return 'Sunday'
    }
    if(date===1){
        return 'Monday'
    }
    if(date===2){
        return 'Tuesday' 
       }
    if(date===3){
        return 'Wednesday'
        }
    if(date===4){
        return 'Thursday'
    }
    if(date===5){
        return 'Friday'
    }
    if(date===6){
        return 'Saturday'
    }
}